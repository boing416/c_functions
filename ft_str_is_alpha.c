int		ft_str_is_alpha(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c == ' ' || ((c < 65 || c > 90) && (c < 97 || c > 122)))
			return (0);
		i++;
	}
	return (1);
}
