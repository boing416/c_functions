char	*ft_strcapitalize(char *str)
{
	int		i;
	char	p;
	char	p2;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		p = str[i - 2];
		p2 = str[i - 1];
		c = str[i];
		if ((i == 0 || p2 == '-' || p == ',' || p == ';' || p2 == ' '
					|| p2 == '+') && (c > 96 && c < 123))
		{
			str[i] = str[i] - 32;
		}
		else
		{
			if (i != 0 && p2 != ' ' && (c > 64 && c < 91))
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
