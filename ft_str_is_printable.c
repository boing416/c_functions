int		ft_str_is_printable(char *str)
{
	int		i;
	char	c;
	char	p;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		p = str[i + 1];
		if (c == '\\')
			return (0);
		if (c != ' ' && (c < 33 || c > 126))
			return (0);
		i++;
	}
	return (1);
}
